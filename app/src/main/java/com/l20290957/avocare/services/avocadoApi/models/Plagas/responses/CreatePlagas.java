package com.l20290957.avocare.services.avocadoApi.models.Plagas.responses;

public class CreatePlagas {
    private String nomPlagas;
    private String img;
    private String infoPlag;
    private String recomendacionPlag;

    public String getNomPlagas() { return nomPlagas; }
    public void setNomPlagas(String value) { this.nomPlagas = value; }

    public String getImg() { return img; }
    public void setImg(String value) { this.img = value; }

    public String getInfoPlag() { return infoPlag; }
    public void setInfoPlag(String value) { this.infoPlag = value; }

    public String getRecomendacionPlag() { return recomendacionPlag; }
    public void setRecomendacionPlag(String value) { this.recomendacionPlag = value; }
}
